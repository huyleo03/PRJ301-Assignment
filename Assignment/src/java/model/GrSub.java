package model;

/**
 *
 * @author HELLO
 */
public class GrSub {
    int gid;
    String gname;
    String subname;

    public GrSub() {
    }

    public GrSub(int gid, String gname, String subname) {
        this.gid = gid;
        this.gname = gname;
        this.subname = subname;
    }

    public int getGid() {
        return gid;
    }

    public void setGid(int gid) {
        this.gid = gid;
    }

    public String getGname() {
        return gname;
    }

    public void setGname(String gname) {
        this.gname = gname;
    }

    public String getSubname() {
        return subname;
    }

    public void setSubname(String subname) {
        this.subname = subname;
    }

    
    
    
}
